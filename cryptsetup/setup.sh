#!/bin/bash
## This file should be placed on a USB drive and executed to setup.
## That USB drive will unlock your LUKS2 encrypted / during boot.

set -e

export PATH=$PATH:/usr/sbin

hookit() {
## Create a HOOK for initramfs to add the commands needed to boot.
## File location /etc/initramfs-tools/hooks/
mkdir -p etc/initramfs-tools/hooks/
cat << EOF > etc/initramfs-tools/hooks/passphrase-from-usb
#!/bin/sh

PREREQ=""

prereqs() {
        echo "\$PREREQ"
}

case "\$1" in
        prereqs)
                prereqs
                exit 0
        ;;
esac

. "\${CONFDIR}/initramfs.conf"
. /usr/share/initramfs-tools/hook-functions

copy_exec /bin/passphrase-from-usb /bin/passphrase-from-usb
copy_exec /lib/cryptsetup/scripts/passdev /lib/cryptsetup/scripts/passdev
copy_exec /lib/cryptsetup/askpass /lib/cryptsetup/askpass
EOF
echo " Hook Created."
chmod +x etc/initramfs-tools/hooks/passphrase-from-usb
}

scriptit() {
## Create the script that will automaticly enter the passphrase when available
## File location /bin/
mkdir -p bin/
cat << EOF > bin/passphrase-from-usb
#!/bin/sh

set -e
device=\$(echo \$1 | cut -d: -f1)
tries=0

seek() {
while [ \$tries -lt 3 ]; do
    if ! [ -e "\$device" ]; then
        echo "Waiting for USB stick to be recognized..." >&2
        sleep 3
    else
	plugged
    fi
tries=\$(( \$tries + 1 ))
done
fallback
}

plugged() {
    if [ -e "\$device" ]; then
        echo "Unlocking the disk \$CRYPTTAB_SOURCE (\$CRYPTTAB_NAME) with USB key" >&2
	sleep 3
        /lib/cryptsetup/scripts/passdev \$CRYPTTAB_KEY
	quit
    else
        echo "Can't find \$device"; echo "Wrong USB stick or USB not present!" >&2
	sleep 3
	fallback
    fi
}

fallback() {
echo " Enter Passphrase or Plug in USB with KEY file and press <Enter>" >&2
/lib/cryptsetup/askpass "Unlocking the disk \$CRYPTTAB_SOURCE (\$CRYPTTAB_NAME) Enter passphrase: "

if ! [ \$? -eq 0 ]; then
plugged
fi
quit
}

quit() {
kill \$THISSCRIPT >/dev/null 2>&1
exit 0
}

seek
THISSCRIPT=\$!
EOF
echo " Script Created "
chmod +x bin/passphrase-from-usb
}

