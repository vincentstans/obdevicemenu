seldisk() {
filter=$( ls /dev/disk/by-id | grep -w 'usb\|USB' | tr '\n' ' ')
disk=($filter)
x=0

for i in "${disk[@]}"; do 
echo "nr $x: ${disk[$x]} "
x=$(( $x + 1 ))
done 

read -p "What disk to use to store the Key? " usedisk
if [[ $usedisk -gt ${#disk[@]} ]]; then
	echo
	echo " Not a valid value"
else
	deuse=$(blkid /dev/disk/by-id/${disk[$usedisk]} | grep -ow -P 'UUID=\K[^ ]*' | sed -e 's/=//' -e 's/\"//g')
	echo $deuse
	useusbdev=${disk[$usedisk]}
	echo 
#	echo "Run: "
#	echo "sudo e2label $useusbdev USB-KEY"
fi
}

edtcryp() {
	crypted=$(head -1 /etc/crypttab | cut -d_ -f1)
	echo
	echo " Adjust /etc/crypttab accordingly"; echo " Replace discard with keyscript=/bin/passphrase-from-usb"
	echo " Replace none with /dev/disk/by-uuid/$deuse:/key.jpg "; echo
    head -1 /etc/crypttab | sed -e 's/none/\/dev\/disk\/by-uuid\/'"$deuse"':\/key.jpg/' -e 's/discard/keyscript=\/bin\/passphrase-from-usb/'	> etc/crypttab
	cat etc/crypttab
	echo
}

